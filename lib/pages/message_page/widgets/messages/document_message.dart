import 'dart:ui';

import 'package:flutter/material.dart';
import 'package:line_icons/line_icons.dart';

import '../../../../constants/colors.dart';
import '../../../../models/document.dart';
import '../../../../models/size_config.dart';

class DocumentMessage extends StatefulWidget {
  final Document file;
  final bool fromFriend;

  const DocumentMessage({super.key, required this.file, required this.fromFriend});

  @override
  State<DocumentMessage> createState() => _DocumentMessageState();
}

class _DocumentMessageState extends State<DocumentMessage> {
  late Document file;
  late bool fromFriend;

  @override
  void initState() {
    file = widget.file;
    fromFriend = widget.fromFriend;
    super.initState();
  }

  @override
  Widget build(BuildContext context) {
    return Padding(
      padding: const EdgeInsets.only(bottom: 20),
      child: Row(
        mainAxisAlignment: fromFriend ? MainAxisAlignment.start : MainAxisAlignment.end,
        children: <Widget>[
          Container(
            width: SizeConfig.screenWidth * 0.8,
            height: 90,
            decoration: BoxDecoration(
              borderRadius: BorderRadius.only(bottomLeft: Radius.circular(fromFriend ? 0 : 40), topLeft: const Radius.circular(40), topRight: const Radius.circular(40), bottomRight: Radius.circular(fromFriend ? 40 : 0)),
              color: fromFriend ? const Color(0xFF313131) : null,
              gradient: fromFriend ? null : LinearGradient(colors: <Color>[greenGradient.lightShade, greenGradient.darkShade]),
            ),
            child: Column(
              children: <Widget>[
                const SizedBox(height: 10),
                Expanded(
                  child: ClipRRect(
                    borderRadius: const BorderRadius.all(Radius.circular(40)),
                    child: BackdropFilter(
                      filter: ImageFilter.blur(sigmaX: 8.0, sigmaY: 8.0),
                      child: Container(
                        width: (SizeConfig.screenWidth * 0.8) - 20,
                        decoration: BoxDecoration(color: Colors.grey.shade200.withValues(alpha: 0.5), borderRadius: const BorderRadius.all(Radius.circular(40))),
                        child: Row(
                          children: <Widget>[
                            const SizedBox(width: 10),
                            SizedBox(
                              width: 36,
                              height: 36,
                              child: Stack(
                                children: <Widget>[
                                  Icon(Icons.insert_drive_file, size: 36, color: fromFriend ? Colors.white70 : Colors.black87),
                                  Center(child: Text(file.fileType.toUpperCase(), style: TextStyle(fontWeight: FontWeight.w700, fontSize: 9, color: fromFriend ? Colors.black54 : grayColor(SizeConfig.ctx).lightShade))),
                                ],
                              ),
                            ),
                            const SizedBox(width: 8),
                            Text(
                              file.fileName.length > 20 ? "${file.fileName.substring(0, 8)}...${file.fileName.substring(file.fileName.length - 9, file.fileName.length)}" : file.fileName,
                              style: TextStyle(color: fromFriend ? Colors.white : Colors.black),
                            ),
                            const Spacer(),
                            Icon(LineIcons.download, color: fromFriend ? Colors.white70 : Colors.black87),
                            const SizedBox(width: 10),
                          ],
                        ),
                      ),
                    ),
                  ),
                ),
                Padding(
                  padding: const EdgeInsets.symmetric(vertical: 3.0),
                  child: Row(
                    children: <Widget>[
                      SizedBox(width: fromFriend ? 0 : 10),
                      Padding(padding: const EdgeInsets.only(left: 16.0), child: Text("${(file.bytesSize / 1024 / 1000).toStringAsFixed(2)} MB", style: TextStyle(color: fromFriend ? Colors.white70 : Colors.black87, fontSize: 12))),
                      const SizedBox(width: 10),
                      const Spacer(),
                      Text("7:31 PM", style: TextStyle(fontSize: 12, color: fromFriend ? Colors.white70 : Colors.black87)),
                      SizedBox(width: fromFriend ? 30 : 10),
                    ],
                  ),
                ),
              ],
            ),
          ),
        ],
      ),
    );
  }
}
